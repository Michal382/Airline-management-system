    private void AnulujMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AnulujMouseClicked
        // TODO add your handling code here:
        if(AnulowanieNrLotu.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Brakuje danych!");
        }else{
            try{
                Licznik();
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root","");
                PreparedStatement Add = Con.prepareStatement("insert into anulowanie values(?, ?, ?, ?)");
                Add.setInt(1, AnulID);
                Add.setInt(2, Integer.valueOf(AnulowanieBiletID.getSelectedItem().toString()));
                Add.setString(3, AnulowanieNrLotu.getText());
                Add.setString(4, AnulowanieData.getDate().toString());
                
                int wiersz = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Bilet został anulowany"); 
                Con.close();
                Anuluj();
                WyswietlAnulowania();
                //Czyszczenie();
            } catch(Exception e){
                JOptionPane.showMessageDialog(this,e);
            }
            
        }
    }//GEN-LAST:event_AnulujMouseClicked

