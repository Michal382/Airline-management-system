    private void PobierzNrLotu()
    {
        String Zapytanie = "select * from rezerwacjatabela where BiletID ="+AnulowanieBiletID.getSelectedItem().toString();
        Statement St;
        ResultSet Rs;
        try{
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root","");
            St = Con.createStatement();
            Rs = St.executeQuery(Zapytanie);
            if(Rs.next())
            {
                AnulowanieNrLotu.setText(Rs.getString("LotNUMER"));
            }
        } catch(Exception e){
        }
    }
    
    private void PobierzBilet()
    {
         try{
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root","");
            St = Con.createStatement();
            String Zapytanie = "select * from rezerwacjatabela";
            Rs = St.executeQuery(Zapytanie);
            while(Rs.next())
            {
                String BiletID = String.valueOf(Rs.getString("BiletID"));
                AnulowanieBiletID.addItem(BiletID);
            }
        } catch (Exception e){
            
        }
    }
    
    int AnulID = 0;
    private void WyswietlAnulowania()
    {
        try{
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root","");
            St = Con.createStatement();
            Rs = St.executeQuery("select * from anulowanie");
            AnulowanieTabela.setModel(DbUtils.resultSetToTableModel(Rs));
        } catch (Exception e){
            
        }
    }
    private void Licznik()
    {
        try{
            St1 = Con.createStatement();
            RS1 = St1.executeQuery("select Max(AnulID) from anulowanie");
            RS1.next();
            AnulID = RS1.getInt(1)+1;
        }catch (Exception e){
            
        }
    }
    
    private void Anuluj()
    {
         try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline", "root","");
                String ZapytanieSQL = "Delete from rezerwacjatabela where BiletID="+AnulowanieBiletID.getSelectedItem();
                Statement Del = Con.createStatement();
                Del.executeUpdate(ZapytanieSQL);
            } catch(Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
    }
    
